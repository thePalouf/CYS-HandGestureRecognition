std::vector<cv::Rect> Camera::faceDetection(){
    cv::Mat img_gray;
    std::vector<cv::Rect> faces;
    cv::cvtColor(m_imageWebcamBGR, img_gray, CV_BGR2GRAY);
    cv::equalizeHist(img_gray, img_gray);
    //trouver les bons paramètres, bon compromis pour detecter visage
    //http://stackoverflow.com/questions/20801015/recommended-values-for-opencv-detectmultiscale-parameters
    //etant donné qu'il ne faut pas être éloigné, ça ne sert à rien de chercher des tetes trop petites
    //donc cvSize() peut être un poil elevé et on gagne en temps de calcul
//CvSize min et max
    m_faceClassifier.detectMultiScale(img_gray, faces, 1.05, 6,
                                     CV_HAAR_SCALE_IMAGE|CV_HAAR_DO_CANNY_PRUNING,cvSize(125,125),cvSize(300,300));

    return faces;
}

void Camera::faceRemoval(const std::vector<cv::Rect> & faces){
    for(size_t i = 0; i < faces.size(); i++){
        cv::Point center(faces[i].x + faces[i].width*0.5, faces[i].y + faces[i].height*0.5);
        //negative thickness to fill the object
        cv::ellipse(m_imageYCrCb, center, cv::Size( faces[i].width*0.5, faces[i].height*0.5), 0, 0, 360, cv::Scalar( 0, 0, 0 ),-1);
    }
}

cv::Mat Camera::cannyEdge(int alpha, int beta, int lowThreshold, int highThreshold, int kernelSize){
///increase camera contrast
    float alphaf = float(alpha)/100.f;
    cv::Mat cont,img_gray, dst;
    cv::cvtColor(m_imageWebcamBGR, img_gray, CV_BGR2GRAY);
    img_gray.convertTo(cont, -1, alphaf, beta);

///apply canny filter with contrast image
    cv::Canny(cont,dst,lowThreshold, highThreshold, kernelSize);

    //trying to draw thick lines after canny filter
    cv::Mat element = getStructuringElement(cv::MORPH_ELLIPSE, cv::Size(2,2));
    morphologyOpening(dst,element,0,1);

    return dst;
}

cv::Mat Camera::skinExtraction(int sYb, int sYh, int sCrb, int sCrh, int sCbb, int sCbh) const {
    cv::Mat mat_skin = cv::Mat::zeros(m_imageYCrCb.rows, m_imageYCrCb.cols, CV_8UC1);
    //YCrCb treshold
    for (int i = 0; i < m_imageYCrCb.rows; i++){
        for (int j = 0; j < m_imageYCrCb.cols; j++){
            //Y
            if ((sYb <= m_imageYCrCb.at<cv::Vec3b>(i, j)[0] && m_imageYCrCb.at<cv::Vec3b>(i, j)[0] <= sYh) &&
                //Cr
                (sCrb <= m_imageYCrCb.at<cv::Vec3b>(i, j)[1] && m_imageYCrCb.at<cv::Vec3b>(i, j)[1] <= sCrh) &&
                //Cb
                (sCbb <= m_imageYCrCb.at<cv::Vec3b>(i, j)[2] && m_imageYCrCb.at<cv::Vec3b>(i, j)[2] <= sCbh))
                mat_skin.at<uchar>(i, j) = 255;
        }
    }

    return mat_skin;
}

void Camera::morphologyOpening(cv::Mat& image, const cv::Mat & element, int nbErode, int nbDilate) const{
    cv::erode(image,image,element,cv::Point(-1,-1),nbErode);
    cv::dilate(image,image,element,cv::Point(-1,-1),nbDilate);
}

void Camera::morphologyClosing(cv::Mat& image, const cv::Mat & element, int nbDilate,int nbErode) const{
    cv::dilate(image,image,element,cv::Point(-1,-1),nbDilate);
    cv::erode(image,image,element,cv::Point(-1,-1),nbErode);
}


